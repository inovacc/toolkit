// mergeByteValues converts hex characters x1 and x2 into a byte.
func mergeByteValues(x1, x2 byte) (byte, bool) {
	b1 := valueMap[x1]
	b2 := valueMap[x2]
	return (b1 << 4) | b2, b1 != 255 && b2 != 255
}
